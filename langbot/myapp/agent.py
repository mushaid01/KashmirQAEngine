def run_pipeline(question: str) -> str:
    """
    Runs the entire RAG pipeline for a given question,
    printing each node's output as it is produced.
    
    Returns the final generated text if found, otherwise an empty string.
    """
    inputs = {"question": question}
    final_generation = ""  # no default fallback, just empty if never updated
    
    for output in app.stream(inputs):
        for key, value in output.items():
            
            # If this node produced a 'generation', store it
            if "generation" in value:
                final_generation = value["generation"]
    
    return final_generation
